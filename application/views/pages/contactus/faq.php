                                            <div class="faq">NATIONAL AGRICULTURE MARKET (NAM)</div>
                                            <div class="faq1">Now Farmers’ Produce will also be sold on internet </div>
                                            <div style="text-align:justify">

                                                <p style="font-weight:bold;">1. What is the National Agriculture Market (NAM) ?</p>
                                                <p>
                                                    NAM is envisaged as a pan-India electronic trading portal which seeks to network the existing APMC and other market yards to create a unified national market for agricultural commodities. NAM is a “virtual” market but it has a physical market (mandi) at the back end.
                                                </p>

                                                <p style="font-weight:bold;">2. What is the difference between NAM and the existing mandi system? </p>
                                                <p>
                                                    NAM is not a parallel marketing structure but rather a device to create a national network of physical mandis which can be accessed online. It seeks to leverage the physical infrastructure of the mandis through an online trading portal, enabling buyers situated even outside the State to participate in trading at the local level.
                                                </p>

                                                <p style="font-weight:bold;">3. Why is NAM necessary? </p>
                                                <p>
                                                    It is necessary to create NAM to facilitate the emergence of a common national market for agricultural commodities. Current APMC regulated market yards limit the scope of trading in agricultural commodities at the first point of sale (i.e. when farmers offer produce after the harvest) in the local mandi, typically at the level of Taluka / Tahsil or at best the district. Even one State is not a unified agricultural market and there are transaction costs on moving produce from one market area to another within the same State. Multiple licences are necessary to trade in different market areas in the same State. All this has led to a highly fragmented and high-cost agricultural economy, which prevents economies of scale and seamless movement of agri goods across district and State borders. NAM seeks to address and reverse this process of fragmentation of markets, ultimately lowering intermediation costs, wastage and prices for the final consumer. It builds on the strength of the local mandi and allows it to offer its produce at the national level.
                                                </p>
                                                <p style="font-weight:bold;">4. How will NAM operate? </p>
                                                <p>The NAM electronic trading platform has been created with an investment by the Government of India (through the Ministry of Agriculture & Farmers’ Welfare). It offers a “plug-in” to any market yard existing in a State (whether regulated or private). The special software developed for NAM is available to each mandi which agrees to join the national network free of cost with necessary customization to conform to the regulations of each State Mandi Act. </p>
                                                <p style="font-weight:bold;">5. Are there any conditions for joining NAM? </p>
                                                <p>States interested to integrate their mandis with NAM are required to carry out following reforms in their APMC Act. </p>
                                                <p>a) Specific provision for electronic trading .</p>
                                                <p>b) Single trading licenses valid for trading in all mandis of the State. </p>
                                                <p>c) Single point levy of transaction fee. </p>
                                                <p style="font-weight:bold;">6. Will the APMC mandis lose out business due to NAM?

                                                </p>
                                                <p>No. NAM basically increases the choice of the farmer when he brings his produce to the mandi for sale. Local traders can bid for the produce, as also traders on the electronic platform sitting in other States. The farmer may chose to accept either the local offer or the online offer. In either case the transaction will be on the books of the local mandi and they will continue to earn the transaction fee. In fact, the volume of business will significantly increase as there will be greater competition for specific produce, resulting in higher transaction fees for the mandi. </p>
                                                <p style="font-weight:bold;">7. Who will bear the costs of NAM? </p>
                                                <p>The national level platform has been developed by the Ministry of Agriculture & Farmers’ Welfare, which will also bear the maintenance costs. As stated above, the integration costs for local mandis and customization of software, training etc. will also be paid for by the Ministry of Agriculture & Farmers’ Welfare as a one-time grant at the time of accepting the mandi in the national network. Thereafter, the running costs of the software at the local level, staff costs for quality check etc. will be met from the transaction fee to be generated through the sale of produce. The intention is to avoid any upfront investment by the mandi when it integrates into NAM, and also enable it to support the running cost through additional generation of revenue.</p>
                                                <p style="font-weight:bold;">8. Who will actually operate the NAM platform? </p>
                                                <p>Ministry of Agriculture & Farmers’ Welfare, Govt. of India has appointed Small Farmers’ Agribusiness Consortium (SFAC) as the Lead Implementing Agency of NAM. SFAC will operate and maintain the NAM platform with the help of a Strategic partner selected for the purpose.</p>
                                                <p style="font-weight:bold;">
                                                    9. What are the likely benefits of NAM?

                                                </p>
                                                <p>NAM is envisaged as a win-win solution for all stakeholders. For the farmers, NAM promises more options for sale at his nearest mandi. For the local trader in the mandi, NAM offers the opportunity to access a larger national market for secondary trading. Bulk buyers, processors, exporters etc. benefit from being able to participate directly in trading at the local mandi level through the NAM platform, thereby reducing their intermediation costs. The gradual integration of all the major mandis in the States into NAM will ensure common procedures for issue of licenses, levy of fee and movement of produce. In the near future we can expect significant benefits through higher returns to farmers, lower transaction costs to buyers and stable prices and availability to consumers. The NAM will also facilitate the emergence of integrated value chains in major agricultural commodities across the country and help to promote scientific storage and movement of agri commodities.</p>
                                                <p style="font-weight:bold;">10. Current Status</p>
                                                <p>As on 31st Oct 2017, 470 Mandis(<a href="mandis.html" title="Click here for Mandi List">Click here for Mandi List</a>) across 14 states have been intergrated with e-NAM namely, Himachal Pradesh (19 Mandis), Haryana (54 Mandis), Rajasthan (25 Mandis), Gujarat (40 Mandis), Maharashtra (45 Mandis), Madhya Pradesh (58 Mandis), Chhattisgarh (14 Mandis), Andhra Pradesh (22 Mandis), Telangana (44 Mandis), Jharkhand (19 Mandis), Odissa(10 Mandis),Tamil Nadu (15 Mandis), Uttarakhand(5 Mandis) and Uttar Pradesh (100 Mandis).

                                                </p>
                                                <p style="font-weight:bold;">11. How soon will NAM be functional?</p>
                                                <p> On 14th April, 2016 in 21 Mandis across 8 States Pilot trading of 24 Commodities namely Apples, Potato Onion, Green Peas, Mahua Flower, Arhar whole (Red Gram), Moong Whole (green gram), Masoor whole (lentil), Urad whole (black gram), Wheat, Maize, Chana whole, Bajra, Barley, Jowar, Paddy, Castor Seed, Mustard Seed, Soya bean, Ground nut, Cotton, Cumin, Red Chillies and Turmeric has been launched. As of 31st Oct 2017, 470 Mandis (<a href="mandis.html" title="Click here for Mandi List"> Click here for Mandi List</a>) across 14 states are live and 90 commodities (<a href="commodity.html" title="Click here for Commodity List">Click here for Commodity List </a>) are being traded.</p>
